	public static boolean isImage(String fileNameOrExt) {
		String ext = FileUtil.getExtension(fileNameOrExt);
		if( StringUtil.empty(ext) )
			ext = fileNameOrExt;
		ext = ext.toLowerCase();
		String mime = typeMap.get(ext);
		return mime!=null && mime.indexOf("image/")==0;
	}

